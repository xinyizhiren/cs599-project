"""Markdown report generation."""

from __future__ import annotations

from collections import defaultdict

from .models import CitationCheck, ClaimRecord, EvidenceItem, PaperRecord


def render_report(
    topic: str,
    selected_papers: list[PaperRecord],
    evidence_items: list[EvidenceItem],
    claims: list[ClaimRecord],
    citation_checks: list[CitationCheck],
) -> str:
    evidence_by_paper: dict[str, list[EvidenceItem]] = defaultdict(list)
    for item in evidence_items:
        evidence_by_paper[item.paper_id].append(item)

    check_by_paper = {check.paper_id: check for check in citation_checks}

    lines: list[str] = [
        f"# Literature Review: {topic}",
        "",
        "## 1. Research Background",
        "",
        (
            "This report is generated by ResearchFlow. It summarizes candidate papers, "
            "extracts evidence, and keeps each key claim linked to retrieved sources. "
            "The content should be reviewed by a human researcher before academic use."
        ),
        "",
        "## 2. Core Papers",
        "",
    ]

    for index, paper in enumerate(selected_papers, start=1):
        check = check_by_paper.get(paper.paper_id)
        status = check.status if check else "warning"
        lines.extend(
            [
                f"### P{index}. {paper.title}",
                "",
                f"- Authors: {', '.join(paper.authors)}",
                f"- Year: {paper.year}",
                f"- Source: {paper.source}",
                f"- URL: {paper.url}",
                f"- Citation check: {status}",
                f"- Summary: {paper.abstract}",
                "",
            ]
        )

    lines.extend(["## 3. Key Claims and Evidence", ""])
    if claims:
        lines.append("| Claim ID | Type | Claim | Evidence IDs |")
        lines.append("| --- | --- | --- | --- |")
        for claim in claims:
            evidence_refs = ", ".join(f"`{item}`" for item in claim.evidence_ids) or "None"
            lines.append(
                f"| `{claim.claim_id}` | {claim.claim_type} | {claim.claim_text} | {evidence_refs} |"
            )
        lines.append("")
    else:
        lines.extend(["No claims were generated.", ""])

    lines.extend(["## 4. Method Taxonomy", ""])
    grouped: dict[str, list[EvidenceItem]] = defaultdict(list)
    for item in evidence_items:
        grouped[item.category].append(item)
    for category, items in grouped.items():
        lines.append(f"### {category.replace('_', ' ').title()}")
        lines.append("")
        for item in items[:5]:
            lines.append(f"- {item.claim} (`{item.evidence_id}`)")
        lines.append("")

    lines.extend(["## 5. Comparative Analysis", ""])
    lines.append("| Paper | Main Evidence | Confidence |")
    lines.append("| --- | --- | --- |")
    for paper in selected_papers:
        items = evidence_by_paper.get(paper.paper_id, [])
        if not items:
            continue
        evidence = items[0]
        lines.append(f"| {paper.title} | {evidence.claim} | {evidence.confidence:.2f} |")
    lines.append("")

    lines.extend(["## 6. Research Gaps", ""])
    gap_claims = [claim for claim in claims if claim.claim_type == "Hypothesis"]
    if gap_claims:
        for claim in gap_claims:
            evidence_refs = ", ".join(f"`{item}`" for item in claim.evidence_ids)
            lines.append(f"- {claim.claim_text} Evidence: {evidence_refs}")
    else:
        lines.append("- More full-text evidence is needed before making strong gap claims.")
    lines.append("")

    lines.extend(["## 7. Limitations of This Automated Review", ""])
    lines.extend(
        [
            "- MVP mode may rely on metadata and abstracts rather than full paper text.",
            "- Offline fixture mode is deterministic and intended for demo stability.",
            "- Citation checks validate metadata availability, not peer-review quality.",
            "",
        ]
    )

    lines.extend(["## Citation Checks", ""])
    lines.append("| Check ID | Paper ID | Status | Message |")
    lines.append("| --- | --- | --- | --- |")
    for check in citation_checks:
        lines.append(f"| `{check.check_id}` | `{check.paper_id}` | {check.status} | {check.message} |")
    lines.append("")

    lines.extend(["## References", ""])
    for index, paper in enumerate(selected_papers, start=1):
        authors = ", ".join(paper.authors)
        lines.append(f"[P{index}] {authors}. ({paper.year}). {paper.title}. {paper.url}")

    return "\n".join(lines) + "\n"
